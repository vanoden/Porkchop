    });

    // Only allow numbers
    document.getElementById('verification_code').addEventListener('keydown', function(e) {
        if ((e.key < '0' || e.key > '9') && e.key !== 'Backspace' && e.key !== 'Delete' && e.key !== 'ArrowLeft' && e.key !== 'ArrowRight') e.preventDefault();
    });
</script>
